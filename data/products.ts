export const getProductBySlug = (slug: string): Product | undefined =>
  products.find((product) => product.slug === slug);

export const getProductsByCategory = (categorySlug: string): Product[] =>
  products.filter((product) => product.category === categorySlug);

export const getProductsBySubcategory = (
  categorySlug: string,
  subcategorySlug: string,
): Product[] =>
  products.filter(
    (product) =>
      product.category === categorySlug && product.subcategory === subcategorySlug,
  );

export const getProductsByBrand = (brandSlug: string): Product[] =>
  products.filter((product) => product.brand === brandSlug);

export const getFeaturedProducts = (): Product[] =>
  products.filter((product) => product.featured);

export const getBestsellers = (): Product[] =>
  products.filter((product) => product.bestseller);

export const getHotDeals = (): Product[] =>
  products.filter((product) => product.hotDeal || product.category === "hot-deals");

export const formatPrice = (price: number, locale: "en" | "ur" = "en"): string => {
  if (locale === "ur") {
    return `${price.toLocaleString("sv-SE")} kr`;
  }

  return `${price.toLocaleString("sv-SE")} kr`;
};
